    public IRLoadScene(Game game) {
        this.game = game;
        initComponents();
        this.jTextField2.setText(System.getProperty("user.home") + "\\Irradiance Engine\\");
        centerWindow();
        File folder = new File(ProjectProperties.projectPath + File.separator + "assets" + File.separator + "scenes");
        DefaultListModel dlm = new DefaultListModel();
        File[] listOfFiles = folder.listFiles();

        for (int i = 0; i < listOfFiles.length; i++) {
            if (listOfFiles[i].isDirectory()) {
                dlm.addElement(listOfFiles[i].getName());
            }
        }
        this.jList1.setModel(dlm);
    }

