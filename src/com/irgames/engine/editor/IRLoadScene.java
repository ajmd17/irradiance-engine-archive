    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (localScene.isSelected()) {
            this.game.invoke(new IRRunnable() {

                @Override
                public void run() {
                    game.loadScene(ProjectProperties.projectPath + File.separator + "assets" + File.separator + "scenes" + File.separator + jList1.getSelectedValue().toString());
                }

            });
        } else if (importExt.isSelected()) {
            this.game.invoke(new IRRunnable() {

                @Override
                public void run() {
                    game.loadScene(jTextField2.getText());
                }

            });
        }
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

