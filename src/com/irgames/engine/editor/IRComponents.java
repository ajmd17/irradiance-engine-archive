    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String formattedName = this.jList1.getSelectedValue().toString();
        if (!formattedName.equals("")) {
            formattedName = formattedName.replace("(disabled)", "");
            formattedName = formattedName.trim();
            GameComponent gc = gcWithName(formattedName);
            if (gc != null) {
                if (!gc.isEnabled()) {
                    game.enableComponent(gc);
                    updateList();
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        GameComponent gc = gcWithName(this.jList1.getSelectedValue().toString());
        if (gc != null) {
            if (gc.isEnabled()) {
                game.disableComponent(gc);
                updateList();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

