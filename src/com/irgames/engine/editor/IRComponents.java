    private GameComponent gcWithName(String name) {
        for (GameComponent gc : game.components) {
            if (gc.getName().equals(name)) {
                return gc;
            }
        }
        return null;
    }

    public IRComponents(Game game) {

        initComponents();
        this.game = game;
        updateList();

    }

    void updateList() {
        DefaultListModel dlm = new DefaultListModel();
        for (GameComponent gc : game.components) {
            String enabledText = "";
            if (!gc.isEnabled()) {
                enabledText = " (disabled)";
            }
            dlm.addElement(gc.getName() + enabledText);
        }
        this.jList1.setModel(dlm);
    }

